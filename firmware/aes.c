static const uint8_t rcon[11] = {
    0x00, 0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40, 0x80, 0x1b, 0x36,
};

static uint8_t idx(const uint8_t row, const uint8_t col) { return row + (col * 4); }

static void sub_bytes(aes_block_t state) {
	for (uint8_t i = 0; i < 16; i++)
		state[i] = s_box[state[i]];
}

static void inv_sub_bytes(aes_block_t state) {
	for (uint8_t i = 0; i < 16; i++)
		state[i] = inv_s_box[state[i]];
}

static void shift_rows(aes_block_t state) {
	// 1st row is unshifted

	// 2nd row is shifted one left
	uint8_t temp = state[idx(1, 0)];
	state[idx(1, 0)] = state[idx(1, 1)];
	state[idx(1, 1)] = state[idx(1, 2)];
	state[idx(1, 2)] = state[idx(1, 3)];
	state[idx(1, 3)] = temp;

	// 3rd row is shifted two left
	temp = state[idx(2, 0)];
	state[idx(2, 0)] = state[idx(2, 2)];
	state[idx(2, 2)] = temp;

	temp = state[idx(2, 1)];
	state[idx(2, 1)] = state[idx(2, 3)];
	state[idx(2, 3)] = temp;

	// 4th row is shifted three left (one right)
	temp = state[idx(3, 0)];
	state[idx(3, 0)] = state[idx(3, 3)];
	state[idx(3, 3)] = state[idx(3, 2)];
	state[idx(3, 2)] = state[idx(3, 1)];
	state[idx(3, 1)] = temp;
}

static void inv_shift_rows(aes_block_t state) {
	// 1st row is unshifted

	// 2nd row is shifted one right
	uint8_t temp = state[idx(1, 0)];
	state[idx(1, 0)] = state[idx(1, 3)];
	state[idx(1, 3)] = state[idx(1, 2)];
	state[idx(1, 2)] = state[idx(1, 1)];
	state[idx(1, 1)] = temp;

	// 3rd row is shifted two right (same as two left)
	temp = state[idx(2, 0)];
	state[idx(2, 0)] = state[idx(2, 2)];
	state[idx(2, 2)] = temp;

	temp = state[idx(2, 1)];
	state[idx(2, 1)] = state[idx(2, 3)];
	state[idx(2, 3)] = temp;

	// 4th row is shifted three right (one left)
	temp = state[idx(3, 0)];
	state[idx(3, 0)] = state[idx(3, 1)];
	state[idx(3, 1)] = state[idx(3, 2)];
	state[idx(3, 2)] = state[idx(3, 3)];
	state[idx(3, 3)] = temp;
}

static uint8_t gf_mul(uint8_t a, uint8_t b) {
	uint8_t result = 0;
	while (b > 0) {
		// Repeatedly add a (shifted) for each bit of b
		if (b & 0x01)
			result ^= a;

		const bool reduce = a & 0x80;
		a <<= 1;

		// Reduce the result modulo the characteristic polynomial
		if (reduce)
			a ^= 0x1B;

		b >>= 1;
	}

	return result;
}

static void mix_columns(aes_block_t state) {
	for (uint8_t col = 0; col < 4; col++) {
		// Multiply (in GF(2^8)) by matrix:
		// 2 3 1 1
		// 1 2 3 1
		// 1 1 2 3
		// 3 1 1 2
		const uint8_t orig[4] = {
		    state[idx(0, col)],
		    state[idx(1, col)],
		    state[idx(2, col)],
		    state[idx(3, col)],
		};

		state[idx(0, col)] = gf_mul(orig[0], 2) ^ gf_mul(orig[1], 3) ^ orig[2] ^ orig[3];
		state[idx(1, col)] = gf_mul(orig[1], 2) ^ gf_mul(orig[2], 3) ^ orig[3] ^ orig[0];
		state[idx(2, col)] = gf_mul(orig[2], 2) ^ gf_mul(orig[3], 3) ^ orig[0] ^ orig[1];
		state[idx(3, col)] = gf_mul(orig[3], 2) ^ gf_mul(orig[0], 3) ^ orig[1] ^ orig[2];
	}
}

static void inv_mix_columns(aes_block_t state) {
	for (uint8_t col = 0; col < 4; col++) {
		// Multiply (in GF(2^8)) by matrix:
		// 14 11 13 9
		// 9 14 11 13
		// 13 9 14 11
		// 11 13 9 14
		const uint8_t orig[4] = {
		    state[idx(0, col)],
		    state[idx(1, col)],
		    state[idx(2, col)],
		    state[idx(3, col)],
		};

		const uint8_t coeffs[4] = {14, 11, 13, 9};

		for (uint8_t i = 0; i < 4; i++) {
			uint8_t total = 0;
			for (uint8_t j = 0; j < 4; j++)
				total ^= gf_mul(orig[(i + j) % 4], coeffs[j]);
			state[idx(i, col)] = total;
		}
	}
}

static void add_round_key(aes_block_t state, const aes_key_t key) {
	for (uint8_t i = 0; i < 16; i++)
		state[i] ^= key[i];
}

static void next_key(aes_key_t key, const uint8_t round) {
	assert(round != 0);

	for (uint8_t row = 0; row < 4; row++) {
		const uint8_t rot_byte = key[idx((row + 1) % 4, 3)];
		key[idx(row, 0)] ^= s_box[rot_byte];
	}

	key[idx(0, 0)] ^= rcon[round];

	for (uint8_t col = 1; col < 4; col++)
		for (uint8_t row = 0; row < 4; row++)
			key[idx(row, col)] ^= key[idx(row, col - 1)];
}

void aes_encrypt_block(aes_block_t state, const aes_key_t key) {
	aes_key_t key_int;
	for (uint8_t i = 0; i < 16; i++)
		key_int[i] = key[i];

	add_round_key(state, key_int);

	for (uint8_t i = 1; i < 10; i++) {
		sub_bytes(state);
		shift_rows(state);
		mix_columns(state);
		next_key(key_int, i);
		add_round_key(state, key_int);
	}

	sub_bytes(state);
	shift_rows(state);
	next_key(key_int, 10);
	add_round_key(state, key_int);
}

void aes_decrypt_block(aes_block_t state, const aes_key_t key) {
	aes_key_t keys[11];

	// First key is just the input key
	for (uint8_t i = 0; i < 16; i++) {
		keys[0][i] = key[i];
	}

	for (uint8_t i = 1; i < 11; i++) {
		// Copy previous key, then advance it
		for (uint8_t j = 0; j < 16; j++) {
			keys[i][j] = keys[i - 1][j];
		}
		next_key(keys[i], i);
	}

	// Undo last round
	add_round_key(state, keys[10]);
	inv_shift_rows(state);
	inv_sub_bytes(state);

	for (uint8_t i = 9; i >= 1; i--) {
		add_round_key(state, keys[i]);
		inv_mix_columns(state);
		inv_shift_rows(state);
		inv_sub_bytes(state);
	}

	add_round_key(state, keys[0]);
}

void aes_first_round_s_box(aes_block_t state, const aes_key_t key) {
	add_round_key(state, key);
	sub_bytes(state);
}
